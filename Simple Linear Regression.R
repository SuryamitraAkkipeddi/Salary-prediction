######################################################################################################################
#################################### I> INSTALL PACKAGES #############################################################
######################################################################################################################

install.packages('caTools') 
install.packages('ggplot2')

######################################################################################################################
#################################### II> LOAD THE LIBRARIES ##########################################################
######################################################################################################################

library(caTools)
library(ggplot2)
set.seed(123)

######################################################################################################################
#################################### III> IMPORT THE DATASET  ########################################################
######################################################################################################################

dataset = read.csv('E:\\DESK PROJECTS\\MACHINE LEARNING SUMMARY\\ML DATASETS\\Salary_Data.csv')  
summary(dataset)

######################################################################################################################
################################### IV> SPLIT THE DATASET INTO TRAIN SET AND TEST SET ################################
######################################################################################################################

split = sample.split(dataset$Salary, SplitRatio = 2/3)                                                 
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

######################################################################################################################
################################## V> FIT/TRAIN ML MODEL TO THE TRAINING SET #########################################
######################################################################################################################

regressor = lm(formula = Salary ~ YearsExperience, data = training_set)                                             

######################################################################################################################
################################### VII> PREDICT THE TEST SET RESULTS  ###############################################
######################################################################################################################

y_pred = predict(regressor, newdata = test_set)

######################################################################################################################
################################### VIII> VISUALIZE THE TRAINING SET RESULTS  ########################################
######################################################################################################################

ggplot() +
  geom_point(aes(x = training_set$YearsExperience, y = training_set$Salary),
             colour = 'red') +
  geom_line(aes(x = training_set$YearsExperience, y = predict(regressor, newdata = training_set)),
            colour = 'blue') +
  ggtitle('Salary vs Experience (Training set)') +
  xlab('Years of experience') +
  ylab('Salary')

######################################################################################################################
################################### IX> VISUALIZE THE TEST SET RESULTS  ##############################################
######################################################################################################################
 
ggplot() +
  geom_point(aes(x = test_set$YearsExperience, y = test_set$Salary),
             colour = 'red') +
  geom_line(aes(x = training_set$YearsExperience, y = predict(regressor, newdata = training_set)),
            colour = 'blue') +
  ggtitle('Salary vs Experience (Test set)') +
  xlab('Years of experience') +
  ylab('Salary')
